// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
if (num1 >= num2) {
  return num2;
}
else if (num2 > num1) {
  return num1;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
return (num % 2 === 0);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
string.split(''); //splitting string to loop through array for characters.
var long = [];
for (var i = 0; i < string.length; i++) {
if (string[i] === char) { //if the character matches the array value i then it is pushed to var long
  long.push(string[i]); 
}
}
return long.length; //returns length of long which is a collection of all char in string. 
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  string.split(''); //splitting string to loop through array instead.
  var long = [];
  for (var i = 0; i < string.length; i++) {
    if (string[i] === 'B') {
    long.push(string[i]);
  }
}
return long.length;
}


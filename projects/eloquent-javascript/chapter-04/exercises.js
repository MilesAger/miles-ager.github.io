// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(x, y, step = 1) {
  let rag = [];
  if (x === y || step <= 0){
    return [];
  }
  while (y >= x){
    rag.push(x);
    x += step;
  }
  return rag;
  }



